async function generatePresentationPDF() {
  try {
    console.log('üöÄ Iniciando generaci√≥n del PDF de presentaci√≥n...');
    
    const generator = new PresentationPDFGenerator();
    const doc = await generator.generatePDF();
    
    // Crear directorio si no existe
    const outputDir = path.join(__dirname, '../uploads/documents');
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }
    
    // Guardar el PDF
    const outputPath = path.join(outputDir, 'Presentacion-GrowX5-Profesional.pdf');
    const stream = fs.createWriteStream(outputPath);
    
    doc.pipe(stream);
    doc.end();
    
    return new Promise((resolve, reject) => {
      stream.on('finish', () => {
        console.log('‚úÖ PDF generado exitosamente:', outputPath);
        console.log('üìÑ Archivo disponible en:', outputPath);
        resolve(outputPath);
      });
      
      stream.on('error', (error) => {
        console.error('‚ùå Error al guardar el PDF:', error);
        reject(error);
      });
    });
    
  } catch (error) {
    console.error('‚ùå Error al generar el PDF:', error);
    throw error;
  }
}

